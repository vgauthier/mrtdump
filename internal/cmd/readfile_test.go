func TestReadFileOptions(t *testing.T) {
	//t.Logf("Peer Index Buffer size: %d", len(ValidPeerIndexBuffer))
	buf := append(FileMRTHeader, ValidPeerIndexBuffer...)
	fs := fstest.MapFS{
		"test.mrt": {Data: buf},
	}
	t.Run("Test NewReadFileOptions", func(t *testing.T) {
		ro := NewReadFileOptions(fs, "test.mrt", false, false)
		assert.NotNil(t, ro)

		err := ro.ReadFile()
		assert.NoError(t, err, "ReadFile should not return an error")
		assert.NotNil(t, ro.PeerIndex, "PeerIndex should not be nil after ReadFile")
	})
}
